import numpy as np

# Визначення матриць внутрішніх параметрів для обох камер (можна використовувати середні значення)
W, H = 640, 480
f = 800  # фокусна відстань

K1 = np.array([[f, 0, W / 2],
               [0, f, H / 2],
               [0, 0, 1]])

K2 = np.array([[f, 0, W / 2],
               [0, f, H / 2],
               [0, 0, 1]])

# Матриця обертання між камерами (наприклад, з попередніх обчислень)
R = np.array([[0.93575479, -0.30293271, -0.18054014],
              [0.28316496, 0.95058062, -0.12733453],
              [0.21019175, 0.06803125, 0.9752903]])

# Розрахунок гомографії
H = np.linalg.inv(K2) @ R @ K1

print("Матриця гомографії (H):")
print(H)
